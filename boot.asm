; ===========================================================================
; AxonOS Bootloader (16-bit real mode)
; Loads kernel from sectors 2-33 (32 sectors, 16 KB) to 0x10000
; Enables A20, sets up GDT, enters protected mode, jumps to kernel
; Build: nasm -f bin boot.asm -o boot.bin
; ===========================================================================

[bits 16]
[org 0x7C00]

start:
    cli
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x7C00
    sti

    ; BIOS передаёт номер загрузочного диска в dl – сохраним
    mov [boot_drive], dl

    ; Очищаем экран (опционально)
    mov ax, 0x0003
    int 0x10

    ; Выводим сообщение о загрузке
    mov si, msg_loading
    call print16

    ; Читаем ядро (секторы 2..33) в 0x1000:0x0000 (физический адрес 0x10000)
    mov ah, 0x02        ; функция чтения
    mov al, 32          ; количество секторов (32 = 16 КБ)
    mov ch, 0           ; цилиндр 0
    mov cl, 2           ; начиная с сектора 2
    mov dh, 0           ; головка 0
    mov dl, [boot_drive]
    mov bx, 0x1000
    mov es, bx
    xor bx, bx          ; es:bx = 0x1000:0x0000
    int 0x13
    jc read_error

    ; Включаем A20 (быстрый способ через порт 0x92)
    in al, 0x92
    test al, 2
    jnz a20_on
    or al, 2
    out 0x92, al
a20_on:

    ; Загружаем GDT
    lgdt [gdt_desc]

    ; Переходим в защищённый режим
    mov eax, cr0
    or eax, 1
    mov cr0, eax

    ; Дальний прыжок в 32-битный код
    jmp 0x08:protected_mode

; ---------------------------------------------------------------------------
; 16-битные процедуры
; ---------------------------------------------------------------------------
print16:
    lodsb
    or al, al
    jz .done
    mov ah, 0x0E
    int 0x10
    jmp print16
.done:
    ret

read_error:
    mov si, msg_error
    call print16
    jmp $

; ---------------------------------------------------------------------------
; 32-битный код (защищённый режим)
; ---------------------------------------------------------------------------
[bits 32]
protected_mode:
    ; Настраиваем сегментные регистры (селектор данных 0x10)
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax
    mov esp, 0x90000        ; временный стек

    ; Передаём управление ядру (оно находится по адресу 0x10000)
    jmp 0x08:0x10000

; ---------------------------------------------------------------------------
; Данные (GDT)
; ---------------------------------------------------------------------------
align 4
gdt_start:
    dq 0                    ; нулевой дескриптор
    ; сегмент кода (0x08) – база 0, лимит 4 ГБ, присутствует, 32-битный
    dw 0xFFFF, 0x0000, 0x9A00, 0x00CF
    ; сегмент данных (0x10) – база 0, лимит 4 ГБ, присутствует, 32-битный
    dw 0xFFFF, 0x0000, 0x9200, 0x00CF
gdt_end:

gdt_desc:
    dw gdt_end - gdt_start - 1
    dd gdt_start

; ---------------------------------------------------------------------------
; Переменные и сообщения
; ---------------------------------------------------------------------------
boot_drive: db 0
msg_loading: db "Loading AxonOS kernel...", 0x0D, 0x0A, 0
msg_error:   db "Failed to read kernel from disk.", 0x0D, 0x0A, 0

; ---------------------------------------------------------------------------
; Заполнение до 512 байт и сигнатура
; ---------------------------------------------------------------------------
times 510-($-$$) db 0
dw 0xAA55
